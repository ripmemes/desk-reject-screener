    # the following helper function is entirely generated by AI
    @staticmethod
    def get_page_as_base64_image(pdf_path: str, page_num: int, zoom: float = 2.0) -> str: 
        """
        Renders a specific 1-based page number from a PDF into a crisp PNG 
        and returns its Base64 encoded string wrapper.
        """
        doc = fitz.open(pdf_path)
        
        # Check if the requested page even exists in the document
        if page_num > doc.page_count:
            doc.close()
            raise IndexError(f"Requested page {page_num}, but paper only has {doc.page_count} pages.")
        
        # 1-based page number to 0-based index conversion
        page_index = page_num - 1
        page = doc.load_page(page_index)
        
        
        # Using a Matrix to scale the resolution. 
        # zoom=2.0 doubles the crispness of the rendered text so the LLM can read it easily.
        matrix = fitz.Matrix(zoom, zoom)
        pix = page.get_pixmap(matrix=matrix)
        
        # Convert pixel map directly to standard PNG bytes in memory
        image_bytes = pix.tobytes("png")
        
        doc.close()
        
        # Encode bytes to a clean utf-8 Base64 string wrapper
        base64_string = base64.b64encode(image_bytes).decode("utf-8")
        return base64_string

